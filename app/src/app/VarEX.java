package app;

public class VarEX {
	
	public static void main(String args[]) {
		//변수와 상수
		//한글 변수는 지양할 것 
		int su;
		su = 3;
		
		int a = 10;
		a = 5;
		
		String str = "hello java";
		System.out.println(str);
		str = "문자열";
		char c = 'a';
		
		
		System.out.println(str);
		//자바에선 상수를 표현할 때 final 키워드를 사용합니다.
		final int b = 10;
	}

}
